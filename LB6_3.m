%-----------------------------------------------------

% Ввод параметров
a = input('Введите левую границу интервала a: ');
b = input('Введите правую границу интервала b: ');
n = input('Введите количество элементов массива: ');

% Проверка на отрицательную длину массива
if n <= 0
    disp('Массив не может быть отрицательной или нулевой длины');
    return;
end
%-----------------------------------------------------

% Заполнение массива случайными числами в интервале [a, b]
arr = a + (b - a) * rand(1, n);

% Поиск минимального и максимального элемента вручную
min_y = arr(1);
max_y = arr(1);
for i = 2:n
    if arr(i) < min_y
        min_y = arr(i);
    end
    if arr(i) > max_y
        max_y = arr(i);
    end
end

% Поиск минимального и максимального значения через встроенные функции
min_y_fn = min(arr);
max_y_fn = max(arr);

%-----------------------------------------------------

disp('');
% Вывод элементов массива на экран в виде столбца с нумерацией
disp('Элементы массива:');
for i = 1:n
    disp(['Элемент ' num2str(i) ': ' num2str(arr(i))]);
end

disp('');
disp(['Минимальное значение (ручной поиск): ', num2str(min_y)]);
disp(['Максимальное значение (ручной поиск): ', num2str(max_y)]);

disp('');
disp(['Минимальное значение (функция min): ', num2str(min_y_fn)]);
disp(['Максимальное значение (функция max): ', num2str(max_y_fn)]);

